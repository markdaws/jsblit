var MathHelper = {};

MathHelper.zeroTolerance = 0.000001;

MathHelper.sin = function (v) {

    //TODO: At some point we can swap this out for a faster implementation
    return Math.sin(v);
}

MathHelper.cos = function (v) {
    return Math.cos(v);
}

MathHelper.sqrt = function (v) {
    return Math.sqrt(v);
}